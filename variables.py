""" Variables """

# This is how we declare variables in python

num = 15

# No 'Let' or 'const', this is much more normal and rational in my opinion

# We can freely reassign variables after declaration

my_number = 15
print(my_number)
# prints: 15
my_number = -4
print(my_number)
# prints: -4


# You can't declare a variable without assigning it though

# movie

# returns: NameError: name 'movie' is not defined
# this is illegal syntax that cannot be used

""" NAMING CONVENTIONS """

# in Python, we use snake_case
my_number = 10

# Python can reassign all variables, but convention wise, we use ALL_CAPS
# To communicate variables not intended to be reassigned

